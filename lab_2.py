def get_new_policy(elite_states, elite_actions, n_states, n_actions):
    """Возвращает новую политику на основе частот действий в элитных траекториях"""
    from collections import defaultdict

    new_policy = np.zeros([n_states, n_actions])
    state_action_counts = defaultdict(lambda: defaultdict(int))
    state_counts = defaultdict(int)

    for state, action in zip(elite_states, elite_actions):
        state_action_counts[state][action] += 1
        state_counts[state] += 1

    for state in range(n_states):
        if state_counts[state] > 0:
            for action in range(n_actions):
                new_policy[state, action] = state_action_counts[state][action] / state_counts[state]
        else:
            new_policy[state, :] = 1.0 / n_actions

    return new_policy

def show_progress(rewards_batch, log, percentile, reward_range=[-990, +10]):
    """Отображает прогресс обучения"""
    mean_reward = np.mean(rewards_batch)
    threshold = np.percentile(rewards_batch, percentile)
    log.append([mean_reward, threshold])

    plt.figure(figsize=[12, 4])
    plt.subplot(1, 2, 1)
    plt.plot([x[0] for x in log], label='Mean rewards')
    plt.plot([x[1] for x in log], label='Reward thresholds')
    plt.legend()
    plt.grid()
    plt.xlabel('Iteration')
    plt.ylabel('Reward')

    plt.subplot(1, 2, 2)
    plt.hist(rewards_batch, range=reward_range, bins=20)
    plt.vlines([np.percentile(rewards_batch, percentile)], [0], [100],
               label="percentile", color='red')
    plt.legend()
    plt.grid()
    plt.xlabel('Reward')
    plt.ylabel('Frequency')

    clear_output(True)
    print("mean reward=%.3f, threshold=%.3f" % (mean_reward, threshold))
    plt.show()

