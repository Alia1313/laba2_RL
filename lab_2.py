def initialize_policy(n_states, n_actions):
    """Инициализирует политику равномерным распределением"""
    policy = np.ones([n_states, n_actions]) / n_actions
    return policy

policy = initialize_policy(n_states, n_actions)
assert type(policy) in (np.ndarray, np.matrix)
assert np.allclose(policy, 1./n_actions)
assert np.allclose(np.sum(policy, axis=1), 1)

def generate_session(env, policy, t_max=10**4, epsilon=0.1):
    """
    Играть до конца или t_max тиков.
    :param policy: массив [n_states, n_actions] с вероятностями действий
    :param epsilon: вероятность случайного действия (exploration)
    :returns: список состояний, список действий и сумма наград
    """
    states, actions = [], []
    total_reward = 0.
    s, info = env.reset()

    for t in range(t_max):
        if np.random.random() < epsilon:
            a = env.action_space.sample()
        else:
            a = np.random.choice(n_actions, p=policy[s])

        next_s, r, terminated, truncated, info = env.step(a)
        done = terminated or truncated

        states.append(s)
        actions.append(a)
        total_reward += r
        s = next_s

        if done:
            break

    return states, actions, total_reward

s, a, r = generate_session(env, policy)
